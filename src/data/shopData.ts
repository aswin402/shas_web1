/* Helper to convert Google Drive sharing link to a direct local asset URL */
export function getDriveImageUrl(url: string): string {
  if (!url) return '/images/products/placeholder.webp';
  
  let id = '';
  const fileDMatch = url.match(/\/file\/d\/([a-zA-Z0-9_-]+)/);
  if (fileDMatch) {
    id = fileDMatch[1];
  } else {
    const dMatch = url.match(/\/d\/([a-zA-Z0-9_-]+)/);
    if (dMatch) {
      id = dMatch[1];
    } else {
      const idMatch = url.match(/[?&]id=([a-zA-Z0-9_-]+)/);
      if (idMatch) {
        id = idMatch[1];
      }
    }
  }

  const assetName = idToAssetMap[id];
  if (assetName) {
    return new URL(`../assets/${assetName}.png`, import.meta.url).href;
  }
  
  return url;
}

